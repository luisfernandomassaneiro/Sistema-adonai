    private void texto_CodigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_texto_CodigoKeyPressed
        if(evt.getKeyCode() == 9 || evt.getKeyCode() == 10) {
            if(StringUtils.isNotEmpty(texto_Codigo.getText())) {
                try {
                    produtoLookup = produtoDAO.recuperaProdutoPeloCodigo(texto_Codigo.getText());
                    if(produtoLookup != null) {
                        texto_Descricao.setText(produtoLookup.getDescricao());
                    } else {
                        JOptionPane.showMessageDialog(null, "Produto não encontrado!", "Erro!", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (ValidateException ex) {
                    Logger.getLogger(MovimentacaoEstoque.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro!", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Informe um código!", "Alerta!", JOptionPane.WARNING_MESSAGE);
            }
        }

    }//GEN-LAST:event_texto_CodigoKeyPressed

