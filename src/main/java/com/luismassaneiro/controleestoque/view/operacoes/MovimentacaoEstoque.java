    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean podeGravar = true;
        Integer quantidade = 0;
        if(produtoLookup == null) {
            podeGravar = false;
            JOptionPane.showMessageDialog(null, "Produto não foi informado!", "Erro!", JOptionPane.ERROR_MESSAGE);
        }
        if(StringUtils.isEmpty(texto_Quantidade.getText())) {
            podeGravar = false;
            JOptionPane.showMessageDialog(null, "É obrigatório informar a quantidade!", "Erro!", JOptionPane.ERROR_MESSAGE);
        } else {
            quantidade = Integer.valueOf(texto_Quantidade.getText());
        }

        try {
            if(radio_Saida.isSelected())
            ProdutoHelper.getInstance().verificaQuantidadeEmEstoque(produtoLookup, quantidade);

        } catch (ValidateException ex) {
            podeGravar = false;
            Logger.getLogger(MovimentacaoEstoque.class.getName()).log(Level.SEVERE, null, ex);
            if(StringUtils.isNotEmpty(ex.getMessage()) && MensagemFixas.QUANTIDADE_INDISPONIVEL.toString().equals(ex.getMessage()) &&
                StringUtils.isNotEmpty(ex.getMensagemExcecao())) {
                JOptionPane.showMessageDialog(this, ex.getMensagemExcecao(), "Erro!", JOptionPane.ERROR_MESSAGE);
            }
        }

        if(podeGravar) {
            try {
                ProdutoHelper.getInstance().atualizaQuantidadeAtual(produtoLookup, quantidade, radio_Entrada.isSelected());
                JOptionPane.showMessageDialog(this, "Movimentação realizada com sucesso!", "Finalização", JOptionPane.INFORMATION_MESSAGE);
                limpar();
            } catch (ValidateException ex) {
                Logger.getLogger(MovimentacaoEstoque.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

