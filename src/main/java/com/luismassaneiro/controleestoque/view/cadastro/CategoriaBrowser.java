    private void pesquisar() {
        String descricao = texto_Descricao.getText();
        CategoriaTableModel modelo = new CategoriaTableModel();
        boolean somenteAtivo = flagAtivo.isSelected();
        List<Categoria> listaCategoria = dao.recuperaCategoriaComFiltros(descricao.concat("%"), somenteAtivo);
        modelo.reload(listaCategoria);
        tabela_Categoria.setModel(modelo);
    }
 
    private void openForm(Categoria categoria) {
        if (form == null)
            form = new CategoriaForm();
        form.setModel(categoria);
        GerenciadorJanelas.getInstance().abrirJanela(form).setVisible(true);
    }
  
    private Categoria getCategoriaSelecionada() {
        if (tabela_Categoria.getSelectedRow() == -1)
            return null;
        
        CategoriaTableModel modelo = (CategoriaTableModel) tabela_Categoria.getModel();
        Categoria categoriaSelecionada = modelo.getCategoriaAt(tabela_Categoria.getSelectedRow());
        return categoriaSelecionada;
    }
    
